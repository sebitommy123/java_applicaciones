package ejercision;

public class Ejercisio1 {
	
	public static void main(String[] args) {
		int N = 23; 
		char C = 'C';
		double A = 24.5;
		
		System.out.println("N = " + N);
		System.out.println("C = " + C);
		System.out.println("A = " + A);
		if (A > N){
			System.out.println("N-A = " + (A-N));
		}else{
			System.out.println("N-A = " + (N-A));
		}
		
		System.out.println("N+A = " + (N+A));
		
	}

}
